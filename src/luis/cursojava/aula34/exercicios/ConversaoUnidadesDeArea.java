public class ConversaoUnidadesDeArea {
    
    public static double metroPes(double m){
        return m*10.76;
    }
    
    public static double peCentimetro(double pe){
        return pe *929;
    }
    
    public static double milhaAcres(double m){
        return m*640;
    }
    
    public static double acrePes(double a){
        return a*43560;
    }
}
